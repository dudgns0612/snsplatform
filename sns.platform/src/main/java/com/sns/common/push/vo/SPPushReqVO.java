package com.sns.common.push.vo;

public class SPPushReqVO {

	public SPPushReqVO(){
		
	}
	
	public SPPushReqVO(String type, Integer boardNum){
		this.pushType = type;
		this.boardNum = boardNum;
	}
	
	private String retCode;
	
	private String retMessage;
	
	private String pushType;
	
	private String title;
	
	private String message;
	
	private Integer boardNum;

	/**
	 * @return the retCode
	 */
	public String getRetCode() {
		return retCode;
	}

	/**
	 * @param retCode the retCode to set
	 */
	public void setRetCode(String retCode) {
		this.retCode = retCode;
	}

	/**
	 * @return the retMessage
	 */
	public String getRetMessage() {
		return retMessage;
	}

	/**
	 * @param retMessage the retMessage to set
	 */
	public void setRetMessage(String retMessage) {
		this.retMessage = retMessage;
	}

	/**
	 * @return the pushType
	 */
	public String getPushType() {
		return pushType;
	}

	/**
	 * @param pushType the pushType to set
	 */
	public void setPushType(String pushType) {
		this.pushType = pushType;
	}

	/**
	 * @return the title
	 */
	public String getTitle() {
		return title;
	}

	/**
	 * @param title the title to set
	 */
	public void setTitle(String title) {
		this.title = title;
	}

	/**
	 * @return the message
	 */
	public String getMessage() {
		return message;
	}

	/**
	 * @param message the message to set
	 */
	public void setMessage(String message) {
		this.message = message;
	}

	/**
	 * @return the boardNum
	 */
	public Integer getBoardNum() {
		return boardNum;
	}

	/**
	 * @param boardNum the boardNum to set
	 */
	public void setBoardNum(Integer boardNum) {
		this.boardNum = boardNum;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "SPPushReqVO [retCode=" + retCode + ", retMessage=" + retMessage + ", pushType=" + pushType + ", title="
				+ title + ", message=" + message + ", boardNum=" + boardNum + "]";
	}
	
}
